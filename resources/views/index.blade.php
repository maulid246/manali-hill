<script>
const slider = document.getElementById('slider');
const prevButton = document.getElementById('prev');
const nextButton = document.getElementById('next');

let currentIndex = 0;
const slides = document.querySelectorAll('.slide');
const totalSlides = slides.length;
const slideWidth = 100 / (totalSlides - 1); // Lebar setiap slide

// Set initial position
slider.style.transform = `translateX(-${slideWidth}%)`;

// Function to show slide
function showSlide(index) {
    slider.style.transition = 'transform 0.3s ease-in-out';
    slider.style.transform = `translateX(-${(index + 1) * slideWidth}%)`;

    currentIndex = index;
}

// Function for next slide
function nextSlide() {
    if (currentIndex >= totalSlides - 1) {
        slider.style.transition = 'none'; // Disable transition for instant jump
        currentIndex = 0;
        slider.style.transform = `translateX(-${(currentIndex + 1) * slideWidth}%)`;
        setTimeout(() => {
            slider.style.transition = 'transform 0.3s ease-in-out'; // Re-enable transition
            showSlide(currentIndex + 1);
        }, 20);
    } else {
        showSlide(currentIndex + 1);
    }
}

// Function for previous slide
function prevSlide() {
    if (currentIndex <= 0) {
        slider.style.transition = 'none'; // Disable transition for instant jump
        currentIndex = totalSlides - 2; // Jump to the second-to-last slide
        slider.style.transform = `translateX(-${(currentIndex + 1) * slideWidth}%)`;
        setTimeout(() => {
            slider.style.transition = 'transform 0.3s ease-in-out'; // Re-enable transition
            showSlide(currentIndex - 1);
        }, 20);
    } else {
        showSlide(currentIndex - 1);
    }
}

// Event listeners for buttons
prevButton.addEventListener('click', prevSlide);
nextButton.addEventListener('click', nextSlide);

// Initialize first slide
showSlide(currentIndex);
</script>
